    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
       JTextField dayField = new JTextField();
    JTextField hourField = new JTextField();
    JTextField minuteField = new JTextField();

    Object[] fields = {
        "Day:", dayField,
        "Hour (0-23):", hourField,
        "Minute (0-59):", minuteField,
    };

    int option = JOptionPane.showConfirmDialog(this, fields, "Set Available Time", JOptionPane.OK_CANCEL_OPTION);

    if (option == JOptionPane.OK_OPTION) {
        try {
            int day = Integer.parseInt(dayField.getText());
            int hour = Integer.parseInt(hourField.getText());
            int minute = Integer.parseInt(minuteField.getText());

            int time = hour * 60 + minute;
            DateAndTime dt = new DateAndTime(day, time);

            WorkshopSession session = new WorkshopSession(dt, loggedTrainer);
            session.setTrainerAvailable(true);

            loggedTrainer.getMyClassRoom().addSession(session);
            Data.addSession(session);

            JOptionPane.showMessageDialog(this, "Availability set for Day " + day + " at " + hour + ":" + minute);

            // âœ… Console logging for confirmation
            System.out.println("Trainer " + loggedTrainer.getName() + " set availability for Day " + day + " at " + hour + ":" + minute);

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Please enter valid numbers.");
        }
    }

    }//GEN-LAST:event_jButton4ActionPerformed
 

