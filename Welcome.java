    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         // Show input dialog to get trainer's name
    String trainerName = JOptionPane.showInputDialog(this, "Enter Trainer Name:");

    // Null check in case user clicks "Cancel"
    if (trainerName == null || trainerName.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a valid trainer name.");
        return;
    }

    // Try to find the trainer in system
    Trainer loggedTrainer = Data.getTrainerByName(trainerName.trim());

    if (loggedTrainer != null) {
        System.out.println("âœ… Trainer logged in: " + loggedTrainer.getName());
        new TrainerScreen(loggedTrainer).setVisible(true);
        this.dispose(); // close welcome page
    } else {
        JOptionPane.showMessageDialog(this, "Trainer not found. Please try again.");
    }

    }//GEN-LAST:event_jButton3ActionPerformed

